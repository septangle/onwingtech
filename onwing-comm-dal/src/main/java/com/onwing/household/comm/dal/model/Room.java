package com.onwing.household.comm.dal.model;

public class Room {
	
	private Long id;
	
	private String name;
	
	private Floors floors;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Floors getFloors() {
		return floors;
	}

	public void setFloors(Floors floors) {
		this.floors = floors;
	}


	
	

}
