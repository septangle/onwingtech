package com.onwing.household.comm.dal.model;

public class Floors {
	
	private Long id;
	
	private String name;
	
	private BuildingCell buildingCell;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public BuildingCell getBuildingCell() {
		return buildingCell;
	}

	public void setBuildingCell(BuildingCell buildingCell) {
		this.buildingCell = buildingCell;
	}


	
	
	

}
